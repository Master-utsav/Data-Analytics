
# Assignment Operators
x = 5
print("Initial x:", x)

# Add and assign
x += 3
print("After x += 3:", x)

# Subtract and assign
x -= 2
print("After x -= 2:", x)

# Multiply and assign
x *= 4
print("After x *= 4:", x)

# Divide and assign
x /= 2
print("After x /= 2:", x)

# Modulus and assign
x %= 3
print("After x %= 3:", x)

# Exponentiate and assign
x **= 2
print("After x **= 2:", x)
