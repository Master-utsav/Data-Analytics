
# Bitwise Operators
a = 5  # 0101 in binary
b = 3  # 0011 in binary

# Bitwise AND
print("Bitwise AND:", a & b)

# Bitwise OR
print("Bitwise OR:", a | b)

# Bitwise XOR
print("Bitwise XOR:", a ^ b)

# Bitwise NOT
print("Bitwise NOT:", ~a)

# Left Shift
print("Left Shift:", a << 1)

# Right Shift
print("Right Shift:", a >> 1)
