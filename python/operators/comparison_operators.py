
# Comparison Operators
a = 10
b = 5

# Equal to
print("Equal to: ", a == b)

# Not equal to
print("Not equal to: ", a != b)

# Greater than
print("Greater than: ", a > b)

# Less than
print("Less than: ", a < b)

# Greater than or equal to
print("Greater than or equal to: ", a >= b)

# Less than or equal to
print("Less than or equal to: ", a <= b)
